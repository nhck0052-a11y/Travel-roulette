let currentLang = 'ko'; // Default language

function setLanguage(lang) {
    currentLang = lang;
    document.documentElement.lang = lang;
    updateTextContent();
}

function updateTextContent() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            element.textContent = translations[currentLang][key];
        }
    });

    // Update specific attributes like placeholder or value if needed
    document.title = translations[currentLang].app_title;

    // Update partnership inquiry email and message labels, which were hardcoded as direct text content in the labels
    const emailLabelElement = document.querySelector('label[for="email"]');
    if (emailLabelElement) {
        emailLabelElement.innerHTML = `${translations[currentLang].email_label}<input type="email" name="email" id="email" required>`;
    }
    const messageLabelElement = document.querySelector('label[for="message"]');
    if (messageLabelElement) {
        messageLabelElement.innerHTML = `${translations[currentLang].message_label}<textarea name="message" id="message" required></textarea>`;
    }
}

// Initial language setting
document.addEventListener('DOMContentLoaded', () => {
    // Attempt to get language from local storage, or browser, default to 'ko'
    const storedLang = localStorage.getItem('lang') || navigator.language.split('-')[0] || 'ko';
    setLanguage(storedLang);
});
